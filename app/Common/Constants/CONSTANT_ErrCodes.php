    /**
     * Description: 获取错误码详情
     * Author: WangSx
     * DateTime: 2019-06-21 00:17
     * @param int $code 错误码
     * @param bool $_   默认 false , true 则连带错误码返回
     * @return string
     */
    public static function getError(int $code = 400, bool $_ = false)
    {
        if (!isset(self::$errCodes[$code])) {
            $code = 400;
        }

        return ($_ ? "[{$code}] " : '') . self::$errCodes[$code];
    }

